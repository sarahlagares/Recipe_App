package unit3and4;

public class ForLoops6 {

	public static void main(String[] args) {

		// for loop syntax -> (init; termination condition; increment)
		for (int i = 1; i <= 100; i = i + 1) {
			System.out.print(i + ": ");
			System.out.println(i * i);
		}
	}
		void forLoopExample () {
			for (int i = 1; i <= 100; i = i + 1) {
				System.out.print(i + ": ");
				System.out.println(i * i);
		}

	} 

}
